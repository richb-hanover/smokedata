function hourlies(med, h) {
  const hourVals = [];
  const numSamples = 120;
  for (let i = 0; i < numSamples; i++) {
    const val = Math.round(med+(h+1)*2*(Math.random()-0.5));
    hourVals.push(val);
  }
  console.log(JSON.stringify(hourVals));
  return hourVals;
}

